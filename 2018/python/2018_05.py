#!/bin/env python3

import collections

def swap_case(char):
    if char != char.lower():
        return char.lower()
    else:
        return char.upper()


def main(word):
    stack = collections.deque()
    for char in word:
        if not stack or stack[-1] != swap_case(char):
            stack.append(char)
        else:
            stack.pop()
    print(len(stack))

